def _format_tags(tags: Optional[str]) -> str:
    if not tags:
        return "‚Äî"
    readable = ", ".join(tags_from_csv(tags))
    return readable or "‚Äî"


def format_wish_caption(wish: Wish) -> str:
    lines = [f"<b>{html.escape(wish.title)}</b>"]
    horizon = wish.time_horizon or "–ë–µ–∑ —Å—Ä–æ–∫–∞"
    if wish.due_date:
        horizon = f"{horizon} ‚Äî {wish.due_date.isoformat()}"
    lines.append(f"‚è∞ {html.escape(horizon)}")

    if wish.price_flag:
        amount = wish.price_amount.strip() if wish.price_amount else "–ò–¥–µ—è –±—é–¥–∂–µ—Ç–∞ –µ—Å—Ç—å"
        lines.append(f"üí∞ {html.escape(amount)}")
    else:
        lines.append("üí∞ –ë–µ–∑ –±—é–¥–∂–µ—Ç–∞")

    if wish.tags:
        lines.append(f"üè∑ {html.escape(_format_tags(wish.tags))}")
    else:
        lines.append("üè∑ –ë–µ–∑ —Ç–µ–≥–æ–≤")

    author = wish.user_username or wish.user_first_name or "–∫—Ç–æ-—Ç–æ –∏–∑ –≤–∞—Å"
    lines.append(f"üôã {html.escape(author)}")
    status = "‚úÖ –í—ã–ø–æ–ª–Ω–µ–Ω–æ" if wish.status == "done" else "‚ú® –í –ø—Ä–æ—Ü–µ—Å—Å–µ"
    lines.append(status)
    return "\n".join(lines)


def build_summary_text(summary: Dict[str, object]) -> str:
    lines = ["<b>–°–≤–æ–¥–∫–∞ –ø–æ –∂–µ–ª–∞–Ω–∏—è–º</b>"]
    total_open = int(summary.get("total_open", 0))
    lines.append(f"–û—Ç–∫—Ä—ã—Ç–æ: <b>{total_open}</b>")

    by_horizon: Dict[str, int] = summary.get("by_horizon", {}) or {}
    if by_horizon:
        parts = [f"{html.escape(name)} ‚Äî {count}" for name, count in by_horizon.items()]
        lines.append("–ü–æ —Å—Ä–æ–∫–∞–º: " + ", ".join(parts))
    else:
        lines.append("–ü–æ —Å—Ä–æ–∫–∞–º: –ø–æ–∫–∞ –±–µ–∑ –æ—Ç–º–µ—Ç–æ–∫.")

    nearest: Optional[Wish] = summary.get("nearest")  # type: ignore[assignment]
    if nearest and nearest.due_date:
        lines.append(
            f"–ë–ª–∏–∂–∞–π—à–µ–µ: <b>{html.escape(nearest.title)}</b> ‚Äî {nearest.due_date.isoformat()}"
        )
    elif nearest:
        horizon = nearest.time_horizon or "–±–µ–∑ —Å—Ä–æ–∫–∞"
        lines.append(
            f"–ë–ª–∏–∂–∞–π—à–µ–µ: <b>{html.escape(nearest.title)}</b> ‚Äî {html.escape(horizon)}"
        )
    else:
        lines.append("–ë–ª–∏–∂–∞–π—à–µ–µ: –ø–æ–∫–∞ –Ω–µ—Ç —Ç–æ—á–Ω—ã—Ö –¥–∞—Ç.")

    random_wish: Optional[Wish] = summary.get("random")  # type: ignore[assignment]
    if random_wish:
        lines.append(f"–°–ª—É—á–∞–π–Ω–∞—è –æ—Ç–∫—Ä—ã—Ç–∞—è –∏–¥–µ—è: <b>{html.escape(random_wish.title)}</b>")
    else:
        lines.append("–°–ª—É—á–∞–π–Ω–∞—è –æ—Ç–∫—Ä—ã—Ç–∞—è –∏–¥–µ—è: –¥–æ–±–∞–≤—å—Ç–µ —Ö–æ—Ç—è –±—ã –æ–¥–Ω—É –º–µ—á—Ç—É üíõ")

    motivation = summary.get("motivation")
    if motivation:
        lines.append(f"–ú–æ—Ç–∏–≤–∞—à–∫–∞: {html.escape(str(motivation))}")

    return "\n".join(lines)


def parse_price(text: str) -> object:
    cleaned = text.strip()
    if not cleaned:
        raise ValueError("–°—É–º–º–∞ –Ω–µ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å –ø—É—Å—Ç–æ–π.")
    normalized = cleaned.replace(" ", "").replace(",", ".")
    try:
        value = float(normalized)
    except ValueError:
        return cleaned
    if value.is_integer():
        return int(value)
    return round(value, 2)


def parse_due_date(text: str) -> date:
    cleaned = text.strip()
    try:
        parts = cleaned.split("-")
        if len(parts) != 3:
            raise ValueError
        year, month, day = map(int, parts)
        return date(year, month, day)
    except Exception as exc:  # pragma: no cover - simple validation
        raise ValueError("–î–∞—Ç–∞ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å –≤ —Ñ–æ—Ä–º–∞—Ç–µ YYYY-MM-DD, –Ω–∞–ø—Ä–∏–º–µ—Ä 2024-02-14.") from exc


def toggle_tag(csv: Optional[str], tag: str) -> str:
    tags = tags_from_csv(csv)
    if tag in tags:
        tags.remove(tag)
    else:
        tags.append(tag)
    return ",".join(dict.fromkeys(tags))


def tags_from_csv(csv: Optional[str]) -> List[str]:
    if not csv:
        return []
    return [part.strip() for part in csv.split(",") if part.strip()]


def format_random_idea(idea: Dict[str, object]) -> str:
    title = html.escape(str(idea["title"]))
    description = html.escape(str(idea["description"]))
    tags = ", ".join(idea.get("tags", []))
    horizon = idea.get("default_horizon", "‚è≥ –ö–æ–≥–¥–∞-–Ω–∏–±—É–¥—å")
    lines = [f"<b>{title}</b>", description]
    if tags:
        lines.append(f"üè∑ {html.escape(tags)}")
    lines.append(f"‚è∞ {html.escape(str(horizon))}")
    return "\n".join(lines)


def format_draft_price(flag: Optional[bool], amount: Optional[str]) -> str:
    if flag is None:
        return "–ù–µ —É–∫–∞–∑–∞–Ω–æ"
    if not flag:
        return "–ù–µ—Ç"
    if amount:
        return amount
    return "–ï—Å—Ç—å"


def format_draft_time(time_horizon: Optional[str], due: Optional[date]) -> str:
    if due:
        return f"üóì {due.isoformat()}"
    if time_horizon:
        return time_horizon
    return "–ù–µ –≤—ã–±—Ä–∞–Ω–æ"

